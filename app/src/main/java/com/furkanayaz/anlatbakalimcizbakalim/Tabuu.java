package com.furkanayaz.anlatbakalimcizbakalim;

public class Tabuu {
    private String tabuu;
    private String tabuua;
    private String tabuub;
    private String tabuuc;
    private String tabuud;
    private String tabuue;


    public Tabuu(String tabuu, String tabuua, String tabuub, String tabuuc, String tabuud, String tabuue) {
        this.tabuu = tabuu;
        this.tabuua = tabuua;
        this.tabuub = tabuub;
        this.tabuuc = tabuuc;
        this.tabuud = tabuud;
        this.tabuue = tabuue;
    }

    public String getTabuu() {
        return tabuu;
    }

    public void setTabuu(String tabuu) {
        this.tabuu = tabuu;
    }

    public String getTabuua() {
        return tabuua;
    }

    public void setTabuua(String tabuua) {
        this.tabuua = tabuua;
    }

    public String getTabuub() {
        return tabuub;
    }

    public void setTabuub(String tabuub) {
        this.tabuub = tabuub;
    }

    public String getTabuuc() {
        return tabuuc;
    }

    public void setTabuuc(String tabuuc) {
        this.tabuuc = tabuuc;
    }

    public String getTabuud() {
        return tabuud;
    }

    public void setTabuud(String tabuud) {
        this.tabuud = tabuud;
    }

    public String getTabuue() {
        return tabuue;
    }

    public void setTabuue(String tabuue) {
        this.tabuue = tabuue;
    }
}
